function _toggleLangDropdown() {
  const dd = document.getElementById('lang-dropdown');
  if (!dd) return;
  const open = dd.style.display === 'flex';
  if (open) {
    _closeLangDropdown();
  } else {
    dd.style.display = 'flex';
    // Close on outside click
    setTimeout(() => document.addEventListener('click', _closeLangDropdown, { once: true }), 0);
  }
}

function _closeLangDropdown() {
  const dd = document.getElementById('lang-dropdown');
  if (dd) dd.style.display = 'none';
}

function hideHomeLangPicker() {
  const el = document.getElementById('lang-picker-home');
  if (el) el.style.display = 'none';
}

