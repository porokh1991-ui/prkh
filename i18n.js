// ─── CORE FUNCTIONS ───────────────────────────────────────

function getLang() {
  return localStorage.getItem('fitpro_lang') || 'pt';
}

function setLang(code) {
  if (!I18N[code]) return;
  localStorage.setItem('fitpro_lang', code);
  applyI18n();
  renderHomeLangPicker();
  // Re-render dynamic content if app is already launched
  if (typeof initDailyQuote === 'function') try { initDailyQuote(); } catch(e) {}
  if (typeof getProfile === 'function') {
    const p = typeof getProfile === 'function' ? getProfile() : null;
    if (p && typeof initMotivationWords === 'function') initMotivationWords([p.name]);
    if (typeof renderDashboard === 'function') try { renderDashboard(); } catch(e) {}
    if (typeof renderWorkout === 'function') try { renderWorkout(); } catch(e) {}
    if (typeof renderHistory === 'function') try { renderHistory(); } catch(e) {}
    if (typeof renderProfileTab === 'function') try { renderProfileTab(); } catch(e) {}
    if (typeof renderGroupClasses === 'function') try { renderGroupClasses(); } catch(e) {}
  }
}

function t(key) {
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key] !== undefined ? I18N[lang][key] : null)
      || (I18N['pt'] && I18N['pt'][key] !== undefined ? I18N['pt'][key] : null)
      || key;
}

function getDateLocale() {
  return DATE_LOCALE[getLang()] || 'pt-PT';
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.documentElement.lang = getLang();
  // Update active lang button styles
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === getLang();
    btn.style.background = active ? 'rgba(255,107,53,0.2)' : 'rgba(255,255,255,0.05)';
    btn.style.borderColor = active ? 'var(--orange)' : 'var(--border)';
    btn.style.color = active ? 'var(--orange)' : 'var(--muted)';
  });
}

